#include "../cub3d.h"

int	check_ext(char *str)
{
	int	len;

	len = ft_strlen(str);
	if (str[len - 1] == 'b' && str[len - 2] == 'u' && str[len - 3] == 'c'
		|| str[len - 4] && '.')
		return (1);
	else
		return (0);
}